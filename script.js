function calculateFare() {
    const fromStation = document.getElementById("from-station").value;
    const toStation = document.getElementById("to-station").value;
    const resultElement = document.getElementById("result");

    if (fromStation == 0 || toStation == 0 || fromStation === toStation) {
        resultElement.textContent = "Please select valid stations.";
        return;
    }

    const key = `${fromStation}-${toStation}`;
    const fare = fares[key];

    if (fare) {
        resultElement.textContent = `The fare is BDT ${fare}.`;
    } else {
        resultElement.textContent = "Fare information not available.";
    }
}
